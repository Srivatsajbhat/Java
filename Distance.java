package dc;
public class Distance
{
	public static double MeterToKm(double n)
	{
		return(n*0.001);
	}
	public static double KmToMeter(double n)
	{
		return(n*1000);
	}
	public static double MileToKm(double n)
	{
		return(n*1.60934);
	}
	public static double KmToMile(double n)
	{
		return(n*0.621371);
	}
	public static double MeterToMile(double n)
	{
		return(n*0.000621371);
	}
	public static double MileToMeter(double n)
	{
		return(n*1609.34);
	}
}
