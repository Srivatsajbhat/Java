package cc;
public class Currency
{
	public static double InrToUsd(double n)
	{
		return(n*0.012);
	}
	public static double UsdToInr(double n)
	{
		return(n*81.71);
	}
	public static double InrToEuro(double n)
	{
		return(n*0.011);
	}
	public static double EuroToInr(double n)
	{
		return(n*88.26);
	}
	public static double InrToYen(double n)
	{
		return(n*1.58);
	}
	public static double YenToInr(double n)
	{
		return(n*0.63);
	}
}
